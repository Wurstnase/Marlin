def fsqrt(number):
    root = 0
    bit = 1 << 30

    while bit > number: bit >>= 2

    while bit != 0:
        if number >= root + bit:
            number -= (root + bit)
            root += (bit << 1)

        root >>= 1
        bit >>= 2

    return root


def sqrt7(number):

    number += 127 <<23
    number >>= 1

    return number


